public class HonoluluClamChowder extends ClamChowder {

	public HonoluluClamChowder() {
		this.soupName = "Pacific Clam Chowder";
		this.soupIngredients.clear();        
		this.soupIngredients.add("1 Pound Fresh Pacific Clams");
		this.soupIngredients.add("1 cup pineapple chunks");    
		this.soupIngredients.add("1/2 cup coconut milk");      
		this.soupIngredients.add("1/4 cup SPAM");    
		this.soupIngredients.add("1/4 cup taro chips");     
	}
}
