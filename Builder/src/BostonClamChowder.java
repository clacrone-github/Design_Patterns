public class BostonClamChowder extends ClamChowder {

	public BostonClamChowder() {
		this.soupName = "Quahog Chowder";
		this.soupIngredients.clear();        
		this.soupIngredients.add("1 Pound Fresh Quahogs");
		this.soupIngredients.add("1 cup corn");    
		this.soupIngredients.add("1/2 cup heavy cream");
		this.soupIngredients.add("1/4 cup butter");    
		this.soupIngredients.add("1/4 cup potato chips");
	}
}
