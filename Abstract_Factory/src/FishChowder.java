class FishChowder extends Soup {
    
	public FishChowder() {
		this.soupName = "FishChowder";
		this.soupIngredients.add("1 Pound Fresh fish");
		this.soupIngredients.add("1 cup fruit or vegetables");    
		this.soupIngredients.add("1/2 cup milk");      
		this.soupIngredients.add("1/4 cup butter");    
		this.soupIngredients.add("1/4 cup chips");          
    }
}
