public class SeattleChickenSoup extends ChickenSoup {
	
	public SeattleChickenSoup() {
		this.soupName = "SpicyChickenSoup";
		this.soupIngredients.clear();        
		this.soupIngredients.add("1 Pound diced chicken");
		this.soupIngredients.add("1/2 cup rice");    
		this.soupIngredients.add("1 cup bullion");      
		this.soupIngredients.add("1/16 cup butter");    
		this.soupIngredients.add("1/4 cup diced carrots");
		this.soupIngredients.add("1/4 chili powder");
		this.soupIngredients.add("1/4 cup diced celery");
	}
}
