public class SeattleMinestrone extends Minestrone {

	public SeattleMinestrone() {
		this.soupName = "SausageMinestrone";
		this.soupIngredients.add("1 Pound tomatos");
		this.soupIngredients.add("1/2 cup pasta");    
		this.soupIngredients.add("1 cup tomato juice");
		this.soupIngredients.add("1 Pound italian sausage");
		this.soupIngredients.add("1 can chickpeas");
	}
}
